function createTeleprompter(item, index) {
  const isPhilosophy = item.rubric === "Как думают топ-аналитики";
  const pages = isPhilosophy ? philosophyPages(item, index) : metricPages(item, index);
  const mode = styleModes[index % styleModes.length];
  return {
    ...item,
    mode,
    pages,
    wordCount: pages.map((page) => page.text.join(" ")).join(" ").split(/\s+/).filter(Boolean).length,
  };
}

function mdFor(script) {
  const lines = [
    `# ${script.episode}. ${script.title}`,
    "",
    `Дата: ${script.date}`,
    "",
    `Рубрика: ${script.rubric}`,
    "",
    `Режим драматургии: ${script.mode}`,
    "",
    `Шрифт для печати: 11 pt`,
    "",
    `Объем: 6 страниц`,
    "",
  ];
  script.pages.forEach((page, index) => {
    lines.push(`## Страница ${index + 1} из 6. ${page.label}`, "");
    page.text.forEach((part) => {
      lines.push(part, "");
    });
  });
  return lines.join("\n").trim() + "\n";
}

function htmlFor(script, standalone = true) {
  const body = script.pages.map((page, index) => `
    <section class="tele-page">
      <header>
        <span>${script.episode.toString().padStart(2, "0")} / ${ruDate(script.date)}</span>
        <span>Страница ${index + 1} из 6</span>
      </header>
      <h1>${escapeHtml(script.title)}</h1>
      <p class="meta">${escapeHtml(script.rubric)} · ${escapeHtml(page.label)}</p>
      ${page.text.map((part) => `<p${part.startsWith("[Не читать.") ? ' class="note"' : ""}>${escapeHtml(part)}</p>`).join("\n")}
    </section>
  `).join("\n");
  if (!standalone) return body;
  return `<!doctype html>
<html lang="ru">
<head>
  <meta charset="utf-8">
  <title>${script.episode}. ${escapeHtml(script.title)}</title>
  <link rel="stylesheet" href="../teleprompter-print.css">
</head>
<body>
${body}
</body>
</html>
`;
}

