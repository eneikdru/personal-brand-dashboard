function relatedVideos(index) {
  const previous = episodes[index - 1];
  const next = episodes[index + 1];
  const values = [];
  if (previous) values.push(`прошлый разбор: «${previous.title}»`);
  if (next) values.push(`следующий разбор: «${next.title}»`);
  return values.join("; ");
}

function splitSentences(text) {
  return String(text)
    .split(/(?<=[.!?])\s+/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function paragraph(text) {
  return text.replace(/\s+/g, " ").trim();
}

